public class DiskFileCheck {
    public static final String author = "@author https://github.com/jonedaniel";

    private Long id;

    private String name;

    private String size;

    private String path;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    @Override
    public String toString() {
        return "name : "+name+" , size : "+size+" , path : "+path;
    }
}